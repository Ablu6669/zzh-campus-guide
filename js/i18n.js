    /* ── 2. 状态 ───────────────────────────────── */
    let currentLang = localStorage.getItem('cuhksz_lang') || 'zh';

    /* ── 3. 核心函数 ────────────────────────────── */
    function t(key) {
        const entry = dict[key];
        if (entry && entry[currentLang]) return entry[currentLang];
        if (entry && entry.zh) return entry.zh;
        return null;
    }

    function applyI18n() {
        console.log(`[i18n] Applying language: ${currentLang}`);
        let translatedCount = 0;

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const entry = dict[key];

            if (!entry) {
                console.warn(`[i18n] Missing translation for key "${key}"`);
                return;
            }

            const text = entry[currentLang] || entry.zh;

            if (entry.attr) {
                el.setAttribute(entry.attr, text);
            } else if (entry.html) {
                el.innerHTML = text;
            } else {
                el.textContent = text;
            }
            translatedCount++;
        });

        console.log(`[i18n] Translated ${translatedCount} elements`);

        // 页面语言属性
        document.documentElement.lang = currentLang === 'en' ? 'en' : 'zh-CN';

        // 切换按钮文字
        const btn = document.getElementById('langSwitchBtn');
        if (btn) btn.textContent = currentLang === 'zh' ? 'EN' : '中文';

        // 动态元素：隐藏/显示标记按钮
        syncToggleMarkersBtn();

        // 派发自定义事件，通知其他脚本语言已切换
        document.dispatchEvent(new CustomEvent('i18n:applied', { detail: { lang: currentLang } }));
    }

    /** 同步 toggleMarkers 按钮文字 */
    function syncToggleMarkersBtn() {
        var btn = document.getElementById('toggleMarkers');
        if (!btn) return;
        var markersL = document.getElementById('markersLayer');
        var visible = markersL && markersL.style.opacity !== '0';
        var key = visible ? 'map_toggle_markers_show' : 'map_toggle_markers_hide';
        var entry = dict[key];
        if (entry) btn.textContent = entry[currentLang] || entry.zh || btn.textContent;
    }

    function switchLang(newLang) {
        currentLang = newLang || (currentLang === 'zh' ? 'en' : 'zh');
        localStorage.setItem('cuhksz_lang', currentLang);
        applyI18n();
    }

    function initI18n() {
        applyI18n();
        var btn = document.getElementById('langSwitchBtn');
        if (btn) btn.addEventListener('click', () => switchLang());
    }

    /* ── 4. 暴露接口 ──────────────────────────── */
    window.i18n = { t, switchLang, initI18n, getLang: () => currentLang };

    /* ── 5. 自动初始化 ────────────────────────── */
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initI18n);
    } else {
        initI18n();
    }
